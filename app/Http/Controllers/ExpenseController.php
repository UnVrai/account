use Illuminate\Http\Request;
use Redirect;

class ExpenseController extends Controller
{
    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $expenses = Expense::orderBy('id', 'desc')->paginate(8);
        return view('expense.index', ['expenses' => $expenses]);
    }

    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        $expenseType = Common::where('type', 'expenseType')->get();
        return view('expense.create', ['types' => $expenseType]);
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $expense = new Expense;
        $expense->number = $request->get('number');
        $expense->type = $request->get('type');
        $expense->person = $request->get('person');
        $expense->description = $request->get('description');

        if ($expense->save()) {
            return Redirect::to('/expenses');
        } else {
            return Redirect::back()->withInput()->withErrors('保存失败！');
        }
    }

